/*
 * 1, 3, 2, 6, 3, 1,2 e 1
 */


public class OperadoresDeAtribuicao {
	
	public static void main (String[] args) {
		int a = 1;
		System.out.println(a);
		
		a += 2;
		System.out.println(a);
		
		a -= 1;
		System.out.println(a);
		
		a *= 3;
		System.out.println(a);
		
		a /= 2;
		System.out.println(a);
		
		a /= 2;
		System.out.println(a);
		
		a %= 2;
		System.out.println(a);
	}
}

